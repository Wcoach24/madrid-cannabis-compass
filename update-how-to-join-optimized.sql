## Frequently Asked Questions

### How long does the invitation process take?

**Typical timeline is 24-48 hours from request to approval, plus same-day visit.** Submit [invitation requests](/invitation) 3-7 days before your Madrid arrival for optimal timing *(Source: Average processing timeline from partner clubs, 2024)*. Weekend requests may take longer as clubs process applications during business hours.

### Do I need to know an existing member to join?

**No, Madrid clubs use [online invitation systems](/invitation).** Over 95% of clubs accept online requests from tourists without personal member introductions *(Source: Club survey 2024)*. The modern system makes joining accessible for all international visitors. [Request invitations through our verified directory](/clubs).

### How much does membership cost?

**Membership fees range €20-€60 annually** depending on club tier and location. Budget clubs (€20-€30), mid-range clubs (€40-€50), premium clubs (€50-€70). Product costs are separate at €8-€18 per gram *(Source: Market survey of 40+ clubs, January 2025)*. [See complete cost breakdown](/guide/cannabis-tourism-madrid-guide#budget).

### Can I join if I don''t speak Spanish?

**Yes, most [central clubs](/clubs) have English-speaking staff.** [Tourist-friendly clubs](/guide/best-cannabis-clubs-madrid-2025) in [Malasaña](/clubs?district=Malasaña), [Retiro](/clubs?district=Retiro), and Centro specifically accommodate English-speaking visitors. Use our [club directory filters](/clubs) to find multilingual clubs, or bring translation apps for residential neighborhood clubs.

### Do I need Spanish residency to join?

**No residency required - tourists can join with valid passport.** [Spanish cannabis laws](/guide/cannabis-laws-spain-legal-guide) allow tourists to become club members. Simply show government-issued ID from any country. Tourist visas and foreign passports fully accepted at all clubs on our [verified directory](/clubs).

### Can I join multiple clubs?

**Yes, members commonly join 2-5 clubs.** Each club requires separate membership fee, but you can access all clubs you join. [Weekend visitors](/guide/cannabis-tourism-madrid-guide) typically join 1-2 clubs, while week-long visitors join 3-4 clubs to explore [different neighborhoods](/guide/cannabis-tourism-madrid-guide#neighborhoods) and atmospheres. [Browse clubs by district](/clubs).

### What if my invitation request gets rejected?

**Rejections are rare (less than 5% of legitimate requests).** Usually due to incomplete information or age verification issues *(Source: Partner club approval data, 2024)*. If rejected, resubmit with complete details, verify age meets requirements, or [request from alternative clubs](/clubs). With [20+ verified clubs](/clubs), you have many options.

### How long is membership valid?

**Most clubs offer 1-year memberships renewable annually.** Some clubs provide 6-month tourist memberships. Membership active from payment date until expiration. You can renew easily if returning to Madrid. [Multiple club memberships](/guide/complete-guide-cannabis-clubs-madrid) valid simultaneously with separate expiration dates.

