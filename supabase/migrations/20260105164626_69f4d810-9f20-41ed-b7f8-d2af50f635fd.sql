## Frequently Asked Questions

### Can tourists join cannabis clubs in Madrid?

**Yes, tourists can join cannabis clubs in Madrid**. Unlike Barcelona, which restricted tourist access in 2023, Madrid clubs continue to welcome international visitors. You''ll need valid identification proving you''re 21+ and an invitation from an existing member or authorized platform.

### How much does it cost to join a cannabis club?

**Membership fees typically range from €20-50** as a one-time payment. This covers your registration and membership card. Inside the club, cannabis contributions are separate and vary by product type and quantity.

### How long does the invitation process take?

**The invitation process takes 24-72 hours** on average. During peak tourist season (June-September), allow extra time. We recommend submitting your request at least 3 days before your planned visit.

### Do I need to speak Spanish?

**No, most tourist-friendly clubs have English-speaking staff**. Our [club directory](/clubs) indicates which languages each club supports. Many also have staff who speak French, German, and Italian.

### Can I bring friends to the club?

**Yes, but each person needs their own invitation**. When requesting an invitation, you can include multiple visitors. Each person must be 21+ and provide their own ID for registration.

### Is it legal to join a cannabis club?

**Cannabis clubs operate in a legal gray area** in Spain. While cannabis remains technically illegal, private consumption and cultivation for personal use are decriminalized. Clubs function as private associations where members collectively grow and share cannabis. Read our [complete legal guide](/guide/cannabis-laws-spain-2025) for details.

### What should I bring to my first visit?

**Bring valid photo ID (passport for tourists), your invitation code, and cash for the membership fee**. Most clubs accept only cash for membership registration.

### Can I visit multiple clubs?

**Yes, you can join multiple clubs** if each provides an invitation. However, most visitors find one quality club meets their needs. Our [Best Clubs guide](/guide/best-cannabis-clubs-madrid-2025) helps you choose the right one.

