## Legal Considerations for Club Members

### Understanding Spanish Cannabis Law

**[Spanish cannabis laws create unique legal framework](/guide/cannabis-laws-spain-legal-guide)** for social clubs:

✅ **Legal for club members**:
- Consuming cannabis on club premises
- Contributing to collective cultivation costs
- Membership in multiple clubs
- Possessing member ID card

❌ **Illegal activities**:
- Consuming cannabis in public spaces
- Transporting cannabis outside clubs
- Sharing cannabis with non-members
- Providing cannabis to minors

**Important**: [Public cannabis consumption remains illegal with fines €601-€10,400](/guide/cannabis-laws-spain-legal-guide). Always consume exclusively inside club premises.

