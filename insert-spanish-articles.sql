## Entendiendo el Marco Legal de España

### La Fundación Constitucional

La ley española de cannabis deriva del Artículo 18 de la **Constitución Española**, que protege el derecho a la privacidad y la inviolabilidad del hogar. Esta protección constitucional forma la piedra angular de los derechos de cultivo personal de cannabis y consumo privado.

El marco opera a través de varios instrumentos legales clave:

1. **Código Penal (Artículos 368-378)**: Define delitos de tráfico de drogas
2. **Ley Orgánica 4/2015 de Protección de la Seguridad Ciudadana**: Gobierna sanciones por consumo público
3. **Jurisprudencia del Tribunal Supremo**: Establece parámetros operativos para CSC
4. **Regulaciones Regionales de Cannabis**: Varía por comunidad autónoma

