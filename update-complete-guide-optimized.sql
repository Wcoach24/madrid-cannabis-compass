## Frequently Asked Questions

**1. How long does the club invitation process take?**

For tourists, allow 5-7 days for invitation processing. [Submit your request here](https://weedmadrid.com/invitation) with travel dates. Emergency same-day requests may be possible at select tourist-friendly clubs, though advance planning is strongly recommended.

**2. Can I visit multiple clubs with one membership?**

No, each club requires separate membership. However, you can join multiple clubs if desired. Most tourists focus on 1-2 clubs per visit to maximize the experience and build rapport with staff. [Browse different club options](https://weedmadrid.com/clubs) to find your ideal matches.

**3. What happens if I don''t have a member referral?**

For tourists, [our invitation service](https://weedmadrid.com/invitation) connects you with tourist-friendly clubs that welcome international visitors. For residents, some clubs offer "open days" or accept applications through their websites, though member referrals remain the primary admission method.

**4. Are cannabis clubs safe for tourists?**

Yes, licensed CSCs are safe, regulated environments. All clubs verify member identity, maintain secure premises, and follow strict legal guidelines. Choose [verified tourist-friendly clubs](https://weedmadrid.com/guides/best-cannabis-clubs-madrid-2025) from our listings for the best experience. Staff speak English and understand international members'' needs.

**5. How much should I budget for a club visit?**

Budget €50-100 for your first visit: annual membership fee (€20-60), initial contribution (€10-30), and products (€10-20). Premium clubs may cost more. Monthly visits after registration only require product purchases. [See detailed club pricing](https://weedmadrid.com/clubs) in individual listings.

**6. What''s the difference between standard and premium clubs?**

Premium clubs offer luxury amenities (concierge service, gourmet refreshments, private lounges), higher quality products, and exclusive events. Membership fees are 3-5x higher (€100-200 annually). Standard clubs provide quality experiences with essential amenities. [Compare options here](https://weedmadrid.com/clubs).

**7. Can I consume cannabis in my hotel or Airbnb?**

Private consumption in rental properties is generally permitted if the property owner allows it and you''re not creating public nuisance. However, many hotels and rentals prohibit smoking. Always check property policies first. The safest, most comfortable option is consuming within the club. [Learn about Spanish cannabis laws](https://weedmadrid.com/guides/cannabis-laws-spain-2025).

**8. Do clubs offer non-smoking consumption options?**

Many clubs offer vaporizers, edibles, and beverages. Some have designated vaping-only areas for smoke-free consumption. Ask staff about alternative consumption methods when you register. [Club Wellness](https://weedmadrid.com/clubs/club-wellness) specializes in non-smoking options.

**9. What if I have a bad experience or feel uncomfortable?**

Immediately inform club staff if you feel uncomfortable or experience adverse effects. All clubs have harm reduction protocols and can provide support, quiet spaces, and assistance. Premium clubs often have wellness staff on-site. This is why [choosing reputable, reviewed clubs](https://weedmadrid.com/guides/best-cannabis-clubs-madrid-2025) is essential.

**10. Can I bring friends who aren''t members?**

No, only registered members can enter clubs. Each person must complete their own membership process. Friends can request invitations together for the same club and visit date. [Submit multiple invitation requests](https://weedmadrid.com/invitation) for group visits. Some clubs offer group orientation sessions for new members arriving together.

