## How Cannabis Social Clubs Work

### The Association Model

CSCs function as private non-profit associations. Members collectively fund cannabis cultivation, which is then distributed exclusively among members. [This cooperative model](https://weedmadrid.com/clubs) ensures compliance with Spanish law while providing safe access.

**The CSC Lifecycle:**
1. Member registration with ID verification
2. Association membership approval process
3. Annual membership fee payment (€20-60)
4. Monthly contribution for cultivation costs
5. Access to legal consumption spaces

### Legal Framework

Spain''s "shared consumption" doctrine allows private cultivation for personal use. When individuals collectively organize cultivation through an association, [it remains within constitutional privacy protections](https://weedmadrid.com/guides/cannabis-laws-spain-2025). However, public consumption, commercial sales, and advertising are strictly prohibited.

