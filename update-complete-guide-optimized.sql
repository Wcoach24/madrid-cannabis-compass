## Understanding Cannabis Social Clubs (CSCs)

Cannabis Social Clubs operate under a unique legal framework in Spain. Unlike commercial dispensaries, [these are private member associations](https://weedmadrid.com/guides/cannabis-laws-spain-2025) protected by constitutional privacy rights. The 2015 Supreme Court ruling established clear guidelines: clubs must be non-profit, member-owned, and operate exclusively for personal consumption within private premises.

